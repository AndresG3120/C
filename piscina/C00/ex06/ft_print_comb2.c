#include<unistd.h>

void	ft_maripili(char n[4])
{
	write(1, &n[0], 1);
	write(1, &n[1], 1);
	write(1, " ", 1);
	write(1, &n[2], 1);
	write(1, &n[3], 1);
	if ((n[0] != '9') || (n[1] != '8'))
	{
		write(1, ",", 1);
		write(1, " ", 1);
	}
}

void	ft_print_comb2(void)
{
	char	n[4];

	n[0] = '0';
	while (n[0] <= '9')
	{
		n[1] = '0';
		while (n[1] <= '9')
		{
			n[2] = n[0];
			n[3] = n[1] + 1;
			while (n[2] <= '9')
			{
				while (n[3] <= '9')
				{
					ft_maripili(n);
					n[3]++;
				}
				n[3] = '0';
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
}
